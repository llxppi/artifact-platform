// 检查是否为高频问题
export function getCachedAnswer(artifactId: string, question: string): string | null {
  const cache = frequentQACache[artifactId];
  if (!cache) return null;

  // 模糊匹配
  for (const [key, value] of Object.entries(cache)) {
    if (question.includes(key) || key.includes(question)) {
      return value;
    }
  }

  return null;
}
